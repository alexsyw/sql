drop table if exists j;
drop table if exists p;
drop table if exists s;
drop table if exists spj1;


CREATE TABLE j (
n_izd character(6) NOT NULL /*номер изделия*/ ,
name character(20) /*название изделия*/,
town character(20) /*город изделия*/
	);


CREATE TABLE p (
n_det character(6) NOT NULL  /*номер детали*/,
name character(20)  /*название детали*/,
cvet character(20)  /*цвет детали*/,
ves integer  /*вес детали*/,
town character(20)  /*город детали*/) ;

CREATE TABLE s (
n_post character(6) NOT NULL  /*номер поставщика*/,
name character(20)  /*имя поставщика*/,
reiting integer  /*ранг поставщика*/,
town character(20)  /*город поставщика*/);


CREATE TABLE spj1 (
n_spj character(6) NOT NULL  /*номер поставки*/,
n_post character(6) NOT NULL  /*номер поставщика*/,
n_det character(6) NOT NULL  /*номер детали*/,
n_izd character(6) NOT NULL  /*номер изделия*/,
kol integer  /*количество деталей*/,
date_post date NOT NULL  /*дата поставки*/,
cost integer NOT NULL  /*цена за одну деталь*/);

